package day01_veriables;

public class C2_Variables {
    public static void main (String [] args) {
        int sayi; // ; yazım dilindeki . gibidir. java ;'ü görünce o satırdaki kod yazılımının biittiğini anlar.
        sayi=27;

        System.out.print(sayi);

        char ilkHarf='M';

        System.out.println(ilkHarf);

        double sayiDouble = 5.34;

        System.out.println(sayiDouble);

        System.out.println(sayi + sayiDouble);

        System.out.println(sayi + ilkHarf);
        // Eğer bir toplama işleminde char değşken değeri kullanılırsa karakterin ASCII deeğeri ile toplama yapar






    }
}

